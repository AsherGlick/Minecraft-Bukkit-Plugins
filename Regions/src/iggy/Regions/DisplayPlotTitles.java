package iggy.Regions;

import java.util.HashMap;
import java.util.Map;

import org.bukkit.Bukkit;
import org.bukkit.ChatColor;
import org.bukkit.entity.Player;
import org.bukkit.event.EventHandler;
import org.bukkit.event.EventPriority;
import org.bukkit.event.Listener;
import org.bukkit.event.player.PlayerMoveEvent;


/***************************** DISPLAY PLOT TITLES ****************************\
| This class is made so that when a user enters or leaves a plot the name is   |
| displayed in their chat bar. Pass in the plugin as an argument to the        |
| constructor. To activate the listener run the EnableRegionDispayNames        |
| function and it will automatically grab the names                            |
\******************************************************************************/

public class DisplayPlotTitles implements Listener{
	Map<Player,String> lastLocation = new HashMap<Player,String>();
	Regions plugin;
	
	DisplayPlotTitles (Regions instance){
		plugin = instance;
	}
	
	public void EnableRegionDisplayNames() {
		Bukkit.getPluginManager().registerEvents(this, plugin);
	}
	
	@EventHandler(priority = EventPriority.NORMAL)
	public void playerWalk (PlayerMoveEvent event){
		String plotName = plugin.chunkNames.get(new Position(event.getTo()));
		String playerSpot = lastLocation.get(event.getPlayer());
		if (plotName == null){plotName = "The Wild";}
		if (playerSpot == null){playerSpot = "The Wild";}
		if (plotName != playerSpot){
			lastLocation.put(event.getPlayer(), plotName);
			event.getPlayer().sendMessage("You have entered: "+ChatColor.LIGHT_PURPLE +plotName+ ChatColor.WHITE);
		}
	}
}